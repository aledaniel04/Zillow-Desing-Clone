$(document).ready(function(){
    $(".contenedor-padre").hide()



    for(let i=0; i<propiedad.length; i++){
        $("#casas").append(
            '<div class="casitas">'+
            '<img src="' + propiedad[i].foto + '">'+
            '<p>Precio: ' + propiedad[i].precio + '</p>'+
            '<p>'+propiedad[i].cuartos+' bds | '+propiedad[i].baños+' ba | '+propiedad[i].piesCuadrados+' sqft - House for sale</p>'+
            '<p>Dirección: '+ propiedad[i].direccion + '</p>'+
            '</div>'
        )
    }

    $(".casitas").click(function(){
        let indice = $(this).index()
        $("#precio").text(propiedad[indice].precio)
        $("#direccion").text(propiedad[indice].direccion)
        $("#habitaciones").text(propiedad[indice].cuartos)
        $("#baño").text(propiedad[indice].baños)
        $("#area").text(propiedad[indice].piesCuadrados)
        $("#img1").attr("src", propiedad[indice].foto)
        $("#img2").attr("src", propiedad[indice].foto2)
        $("#img3").attr("src", propiedad[indice].foto3)
        $("#img4").attr("src", propiedad[indice].foto4)
        $("#img5").attr("src", propiedad[indice].foto5)

        $("#popup").show()
    })
    $("#cerrar-popup").click(function(){
        $("#popup").hide()
    })

    $("#vender").click(function(){
        $("#abajo").css("transform", "rotate(180deg)")
    })

    $("#vender").dblclick(function(){
        $("#abajo").css("transform", "rotate(360deg)")
    })
})